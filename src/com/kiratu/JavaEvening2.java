public class JavaEvening2 {
    //executor class
    public static void main(String[] args) {
        //Switch Case Statement
        int x;
        x = 7;
        switch (x){
            case 10:
            case 9:
            case 8:
            case 7:
                System.out.println("The answer is 10");
                break;
            case 11:
                System.out.println("The answer is 11");
                break;

                default:
                    System.out.println("Hapo uko nje brathe");
        }

    }
}
